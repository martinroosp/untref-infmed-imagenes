public class Imagen {

	private String name;
	private byte[] image;

	public String getName() {

		return this.name;
	}

	public void setName(String name) {

		this.name = name;
	}

	public byte[] getImage() {

		return this.image;
	}

	public void setImage(byte[] image) {

		this.image = image;
	}
}
